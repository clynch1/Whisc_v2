    /**
     * A {@link FragmentPagerAdapter} that returns a fragment corresponding to
     * one of the sections/tabs/pages.
     */
    public class SectionsPagerAdapter extends FragmentPagerAdapter {

        public SectionsPagerAdapter(FragmentManager fm) {
            super(fm);
        }

        @Override
        public Fragment getItem(int position) {
            switch (position){
                case 0:
                    Tab1Account tab1 = new Tab1Account();
                    return tab1;
                case 1:
                    Tab2Explore tab2 = new Tab2Explore();
                    return tab2;
                case 2:
                    Tab3Matches tab3 = new Tab3Matches();
                    return tab3;
                default:
                    return null;
            }
        }

        @Override
        public int getCount() {
            // Show 3 total pages.
            return 3;
        }

        @Override
        public CharSequence getPageTitle(int position) {
            switch (position) {
                case 0:
                    return "Account";
                case 1:
                    return "Explore";
                case 2:
                    return "Matches";
            }
            return null;
        }
    }
